// 문석 객체

// HTML 요소들이 객체화가 된 것을 우리는 문서 객체(document object)라고 합니다.

// 노드 

// 문서 객체는 DOM에서 하나의 노드를 담당합니다. 다르게 말해서 노드는 HTML 구성 요소가 객체화 된 문서 객체들로 구성되었다고 말할 수 있습니다.

// DOM 트리 

// 이러한 각각의 노드는 그냥 배치가 되는 게 아니라 자료 구조 중 하나인 트리 구조(tree) 형태로 생성되어 배치됩니다. 이를 DOM 트리라고 합니다.

// DOM

// 문서 객체가 노드가 되고, 노드가 DOM 트리를 구성하면서 웹 브라우저는 하나의 완성된 DOM 형태를 가지게 됩니다. 즉, DOM은 위에서 언급한 모든 개념을 전부 통틀어서 한 데 묶은 포괄적인 용어입니다.